SELECT
    VENDAS.ID_VENDA,
    VENDAS.DATA_VENDA AS "DATA",
    CLIENTES.NOME_CLIENTE AS "CLIENTE",
    PRODUTOS.NOME_PRODUTO AS "PRODUTO COMPRADO",
    PRODUTOS.MARCA
FROM VENDAS
INNER JOIN CLIENTES
    ON VENDAS.ID_CLIENTE = CLIENTES.ID_CLIENTE
INNER JOIN PRODUTOS
    ON VENDAS.ID_PRODUTO = PRODUTOS.ID_PRODUTO
ORDER BY VENDAS.DATA_VENDA;
