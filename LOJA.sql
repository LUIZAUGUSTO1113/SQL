CREATE TABLE CLIENTES(
    ID_CLIENTE INT,
    NOME_CLIENTE VARCHAR2(50),
    SEXO VARCHAR2(1),
    EMAIL VARCHAR2(50),
    DATA_NASCIMENTO DATE
);
CREATE TABLE PRODUTOS(
    ID_PRODUTO INT,
    NOME_PRODUTO VARCHAR2(50),
    MARCA VARCHAR2(50),
    CATEGORIA VARCHAR2(50),
    PRECO_UNIT NUMBER (10, 2),
    CUSTO_UNIT NUMBER (10, 2)
);
CREATE TABLE VENDAS(
    ID_VENDA INT,
    DATA_VENDA DATE,
    ID_CLIENTE INT,
    ID_PRODUTO INT,
    QUANTIDADE NUMBER(10, 2)
);
